# Predicate methods are those methods that end with ? and always return boolean values.

puts 2.even?
puts 10.positive?
puts 3.zero?
puts "Hello".include?("h")

puts 20.between?(10,30)

puts 10.1.floor
puts 10.1.ceil

puts 10.49.round
puts 10.5.round

puts 10.56789.round(2)

puts 10.abs
puts -10.abs